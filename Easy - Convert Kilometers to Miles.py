# This program is to convert Kilometers to Miles using Python

# Get the distance input as Kilometers in float type

kilometers = float(input("Enter the distance in kilometers = "))

# Convert the obtained Kilometers to Miles using the formula

kilometers_to_miles_ratio = 0.621

# Compute the final distance in miles

miles = kilometers*kilometers_to_miles_ratio

# Print the final result

print(f"The stance in miles for the given kilometer distance is {miles}")